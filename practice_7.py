# Built-in Math Functions

import math
x = 9.9

print(math.pi) # value of pi
print(math.e) # value of physics, constant e, exponential constant


result = math.sqrt(x) # find the square root of a value
print(result)

result = math.ceil(x) # find the ceiling, round a floating number up
print(result)

result = math.floor(x) # round a floating number down
print(result)