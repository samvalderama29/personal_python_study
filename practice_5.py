# Basic Arithmetic Operation

# Example:
friends = 10

# For addition:
# friends = friends + 1
# friends += 1
# The shorten operation is called augmented assignment operator

# For subtraction:
# friends = friends - 2
# friends -= 2

# For multiplication
# friends = friends * 3
# friends *= 3

# For division
# friends = friends / 2
# friends /= 2

# For exponents
# friends = friends ** 2
# friends **= 2

# For modulo:
# Modulo is used to give you any remainder of division
remainder = friends % 3
# The % is known as the modulo operator used to give the remainder of division
print(remainder)
# Very popular operator as it is used to find the even or odd

